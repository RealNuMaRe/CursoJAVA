public class DeclaracionesOperadores {
    
    public static void main (String [] args) {
        
        double a = 9;
        double b = 2;
        
        System.out.println("La suma de (a + b) es igual a: " + (a + b));
        System.out.println("La resta de (a - b) es igual a: " + (a - b));
        System.out.println("La multiplicación de (a x b) es igual a: " + (a * b));
        System.out.println("La división de (a / b) es igual a: " + (a / b));
        System.out.println("El resto de la división de (a / b) es igual a: " + (a % b));
        
    }
    
}
