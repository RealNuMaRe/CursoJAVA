public class UsoMath {
    
    public static void main (String [] args) {
        
        //VARIABLES
        double res1 = Math.sqrt(9);
        double res2 = Math.round(5.85);
        double res3 = Math.pow(5, 3);
        
        System.out.println("La raíz cuadrada de 9 es: " + res1);
        System.out.println("El resultado de redondear el número 5.85 es: " + res2);
        System.out.println("Cinco (5) elevado a tres (3) es: " + res3);
        
    }
    
}
